namespace Draw
{
    internal class Line
    {
        private int x;
        private int y;
        private int v1;
        private int v2;

        public Line(int x, int y, int v1, int v2)
        {
            this.x = x;
            this.y = y;
            this.v1 = v1;
            this.v2 = v2;
        }
    }
}